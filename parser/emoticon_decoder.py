def get_face_name(index: int) -> str:
    name = _QQ_FACE_MAP.get(index)
    if name:
        return name
    return f'表情{index}'


def get_face_gif_path(index: int) -> str | None:
    for ext in ('.gif', '.png'):
        path = os.path.join(_FACE_DIR, f'qq-{index}{ext}')
        if os.path.isfile(path):
            return path
    return None


def decode_emoticons_in_text(text: str) -> str:
    if _EMOTICON_PREFIX not in text:
        return text

    result = []
    i = 0
    while i < len(text):
        if text[i] == _EMOTICON_PREFIX and i + 1 < len(text):
            index = ord(text[i + 1])
            face_name = get_face_name(index)
            result.append(f'[{face_name}]')
            i += 2
        else:
            result.append(text[i])
            i += 1

    return ''.join(result)


def get_face_map() -> dict[int, str]:
    return dict(_QQ_FACE_MAP)


def get_face_image_map() -> dict[int, str]:
    result = {}
    for index in _QQ_FACE_MAP:
        for ext in ('.gif', '.png'):
            path = os.path.join(_FACE_DIR, f'qq-{index}{ext}')
            if os.path.isfile(path):
                result[index] = ext
                break
    return result


def decode_emoticons_to_html(text: str) -> str:
    if _EMOTICON_PREFIX not in text:
        return text

    result = []
    i = 0
    while i < len(text):
        if text[i] == _EMOTICON_PREFIX and i + 1 < len(text):
            index = ord(text[i + 1])
            face_name = get_face_name(index)
            gif_path = get_face_gif_path(index)
            if gif_path:
                rel_path = os.path.relpath(gif_path, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
                result.append(f'<img src="{rel_path}" alt="[{face_name}]" title="{face_name}" class="qq-face">')
            else:
                result.append(f'[{face_name}]')
            i += 2
        else:
            result.append(text[i])
            i += 1

    return ''.join(result)
